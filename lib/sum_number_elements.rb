# Sum of Number Elements in an Array
# Arrays can be mixed with various types. Your task 
# for this challenge is to sum all the number elements 
# in the given array. Create a function that takes an 
# array and returns the sum of all numbers in the array.

# Examples
# numbers_sum([1, 2, "13", "4", "645"]) ➞ 3

# numbers_sum([true, false, "123", "75"]) ➞ 0

# numbers_sum([1, 2, 3, 4, 5, true]) ➞ 15

